        /// <summary>
        /// Contenerdor de la clase
        /// </summary>
        private PrecioPACInfo Contexto
        {
             get
              {
                  if (DataContext == null)
                  {
                     InicializaContexto();
                  }
                  return (PrecioPACInfo) DataContext;
                }
                set { DataContext = value; }
