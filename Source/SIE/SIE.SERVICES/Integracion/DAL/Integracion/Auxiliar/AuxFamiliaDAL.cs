        /// <param name="familiaId"></param>
        /// <returns></returns>
        internal static Dictionary<string, object> ObtenerParametroPorID(int familiaId)
        {
            Dictionary<string, object> parametros;
            try
            {
                Logger.Info();
                parametros = new Dictionary<string, object>
                    {
                        {"@FamiliaID", familiaId},
                    };
            }
            catch (Exception ex)
            {
                Logger.Error(ex);
                throw new ExcepcionDesconocida(MethodBase.GetCurrentMethod(), ex);
            }
            return parametros;
        }

